package qedge.oct28;
public class For_EachLoop {
public static void main(String[] args) {
//store integer values into array variable
int num[]={100,300,400,500,600,700,800};
//returns specific value from array collection
System.out.println(num[6]);
for(int each:num)
{
System.out.println(each);	
}
}
}
